 Cypress.Commands.add('login', (email, password) => { 
    cy.get('[data-aut-id="btnLogin"]').click()
    cy.get('[data-aut-id="emailLogin"]').click()
    cy.get('#email_input_field').type(email+'{enter}')
    cy.get('#password"').type(password+'{enter}')
  })
